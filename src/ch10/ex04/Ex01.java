package ch10.ex04;

public class Ex01 {

	public static void main(String[] args) {
		// autoboxing : 기본타입을 wrapper 타입으로 변경
		Integer i = 3; //Integer i = new Integer(3);
		Double d = 3.14;
		Float f = 3.14f;
		Character c = 'a';
		
		
		
		// unboxing : wrapper타입을 기본 타입으로 변경
		int i2 = i + 3;
		double d2 = d * 4;
		float f2  = f-3;
		char c2 = c;
		
		
		
	}

}
