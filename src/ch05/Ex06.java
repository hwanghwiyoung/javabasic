package ch05;

public class Ex06 {

	public static void main(String[] args) {
		// for each
		String[] a= {"s","b","s"};
		for(String str:a) {
			System.out.println(str);
		}
		
		//for each int 타입 출력
		System.out.println("\n");
		int[] b = {1,2,3,4,5};
		for(int i:b) {
			System.out.println(i + " ");
		}
		

	}

}
